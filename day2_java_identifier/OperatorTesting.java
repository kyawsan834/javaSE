class OperatorTesting{
      public static void main(String[] args){
          int numOne = 3;
          int numTwo = 5;
          System.out.println(numOne+numTwo);
          System.out.println(numOne-numTwo);
          System.out.println(numOne*numTwo);
          System.out.println(numOne/numTwo);
          System.out.println(numOne%numTwo);
    }
}